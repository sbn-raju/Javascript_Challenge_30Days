//Data Types

const nums = 2210030034
console.log(nums)//2210030034 as assigned
console.log(typeof(nums))//number

const str = "Github is a Coders library"
console.log(str)//Github is a Coders library as assigned
console.log(typeof(str))//string


const bool = true 
console.log(bool)//true as assigned
console.log(typeof(bool))//boolean


const obj = {
    name:"Raju",
    roll:2210030034,
    class:"Section - 1",
    grade:"A",
}

console.log(obj) //{ name: 'Raju', roll: 2210030034, class: 'Section - 1', grade: 'A' } as assigned
console.log(typeof(obj))//object


const arr = ["Raju", "Viswak", "Dhreej", "Shiva"]
console.log(arr)//[ 1, 2, 3, 4, 5 ]
console.log(typeof(arr))//object

