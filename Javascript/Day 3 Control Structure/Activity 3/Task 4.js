const weekNum = 11

switch(weekNum){
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thrusday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Stauraday");
        break;
    case 7:
        console.log("Stauraday");
        break;
    default:
        console.log("Invalid Number");
}